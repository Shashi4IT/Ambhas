CREATE TABLE address (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT NOT NULL,
    address1 VARCHAR(20),
    address2 VARCHAR(20),
    address3 VARCHAR(20),
    pincode BIGINT NOT NULL,
    city VARCHAR(20),
    district VARCHAR(20),
    state VARCHAR(20) NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);